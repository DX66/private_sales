if (is_array($applied_offers)) {

    if (!isset($discount)) $discount = 0;

    $discount += func_offer_apply_discounts($applied_offers, $products, $offers_order_bonuses);

    foreach ($applied_offers as $offer) {

        if (
            isset($offer['reduce_bp']) 
            && $offer['reduce_bp'] == 'Y' 
            && isset($offer['amount_min'])
        ) {
            $blocked_points += $offer['amount_min'];
        }

        if (!empty($offer['not_used_free_products'])) {
            $not_used_free_products = func_offer_merge_free_products($not_used_free_products, $offer['not_used_free_products']);
        }
    }
