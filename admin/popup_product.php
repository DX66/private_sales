require './auth.php';
require $xcart_dir.'/include/security.php';

x_load('product');

$popup_type = 'P';

include $xcart_dir.'/include/popup_product_category.php';

$cat = intval(@$cat);

if ($cat > 0) {
    $search_query = " AND $sql_tbl[products_categories].categoryid = '$cat'";

    if (!empty($only_regular) && 'Y' === $only_regular) {
        $search_query .= " AND ($sql_tbl[products].product_type = 'N' OR $sql_tbl[products].product_type = '') ";
    }

    $orderby  = "$sql_tbl[products].product";
    $products = func_search_products ($search_query, '', $orderby);

    $smarty->assign ('products', $products);
}

func_display('main/popup_product.tpl',$smarty);
